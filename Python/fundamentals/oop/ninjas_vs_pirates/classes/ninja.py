import random
class Ninja:

    def __init__( self , name ):
        self.name = name
        self.strength = 10
        self.speed = 5
        self.health = 100
    
    def show_stats( self ):
        print(f"Name: {self.name}\nStrength: {self.strength}\nSpeed: {self.speed}\nHealth: {self.health}\n")

    def attack( self , pirate ):
        pirate.health -= self.strength
        return self
    
    def critical_attack( self , pirate ):
        if self.health <= 30:
            self.speed *50/100
            pirate.health -= (self.strength+self.speed)
            return self
    
    def doge( self ):
        print("You missed your attack Pirate!!!")
        self.health += 5
        return self
    
    def combo_attack( self, pirate):
        if self.health == 10:
            pirate.health -= (self.strength+self.speed)*random.randint(1,4)
        return self
    
    def use_weapon(self, weapon):
        if weapon:
            self.critical_attack()
        return self

